/**
 * Helper: Get IUCN status for a species
 */
export function getIucnStatus(speciesId) {
  const code = SPECIES_IUCN[speciesId];
  return code ? IUCN_STATUS[code] : IUCN_STATUS.NE;
}

/**
 * Helper: Get localized name for a species
 */
export function getLocalizedName(speciesId, langCode = 'en') {
  const names = SPECIES_NAMES[speciesId];
  if (!names) {
    // Fallback: format ID as title case
    return speciesId.replace(/_/g, ' ').replace(/\b\w/g, c => c.toUpperCase());
  }
  return names[langCode] || names.en || speciesId.replace(/_/g, ' ');
}

/**
 * Helper: Get distribution regions for a species
 */
export function getDistribution(speciesId) {
  return SPECIES_DISTRIBUTION[speciesId] || [];
}

/**
 * Helper: Get regulations for a species in a region
 */
export function getRegulations(speciesId, regionId) {
  const regs = SPECIES_REGULATIONS[speciesId];
  if (!regs) return null;
  return regs[regionId] || null;
}
